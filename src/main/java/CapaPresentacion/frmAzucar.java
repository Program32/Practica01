    private void btnLeerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLeerActionPerformed
        // TODO add your handling code here:
        //Leer el contenido
        String Valor_Recolectado = txtValorRecolectado.getText();
        String Nombre = txtNombre.getText();
        String Cant = txtCant.getText();
        String Tipo = txtTipo.getText();
        String Cantidad = txtCantidad.getText();
        // Llevar los datos a los atributos del objeto a traves de las propiedades
        azucar.setValor_recolectado(Valor_Recolectado);
        azucar.setNombre(Nombre);
        azucar.setCant(Cant);
        azucar.setTipo(Tipo);
        azucar.setTama(Cantidad);
        JOptionPane.showMessageDialog(rootPane, "El objeto se escribio correctamente");
    }//GEN-LAST:event_btnLeerActionPerformed

